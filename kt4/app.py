"""
Дашборд: «Наши попытки применить Neural SDE».

Простой обзор трёх экспериментов с готовыми графиками, вытащенными из ноутбуков
(neural_sde_ohioT1DM3, wind_neural_sde_final2, threedomens). Ничего не считает на
лету — только показывает результаты. Запуск: streamlit run app.py
"""
from pathlib import Path

import streamlit as st

# Картинки лежат рядом с этим файлом — работает и локально, и на Streamlit Cloud.
try:
    BASE = Path(__file__).parent
except NameError:
    BASE = Path.cwd()
ASSETS = BASE / "attempts_assets"

st.set_page_config(
    page_title="Наши попытки применить Neural SDE",
    layout="wide",
    page_icon="🌀",
)


def show(name, caption=None):
    """Показать картинку из attempts_assets, не падая, если её нет."""
    p = ASSETS / name
    if p.exists():
        st.image(str(p), caption=caption, use_container_width=True)
    else:
        st.info(f"Картинка не найдена: {name}")


# ── Шапка ─────────────────────────────────────────────────────────────────
st.title("Наши попытки применить Neural SDE")
st.markdown(
    "Мы взяли один подход — **нейросеть как дрейф SDE + Эйлер–Маруяма + "
    "KL-регуляризация Гирсанова** (Tzen & Raginsky, 2019) — и попробовали его на "
    "разных задачах. Ниже три попытки: видно, где метод реально помогает, а где "
    "классические модели не уступают."
)

tab1, tab2, tab3 = st.tabs([
    "1. Три домена сразу",
    "2. Прогноз ветрогенерации",
    "3. Глюкоза (OhioT1DM)",
])

# ── Попытка 1: три домена ─────────────────────────────────────────────────
with tab1:
    st.header("Один подход на трёх рядах")
    st.markdown(
        "Задача всюду сведена к one-step forecasting: по окну прошлых значений и "
        "контексту прогнозируем следующее значение. Сравниваем Neural SDE с "
        "классикой (ARIMA / SARIMA / persistence)."
    )

    st.subheader("Сводка по доменам (RMSE, внутри домена — на одной шкале)")
    st.markdown(
        "| Домен | Neural SDE | Лучшая классика | Кто выиграл |\n"
        "|---|---|---|---|\n"
        "| Акции AAPL (лог-доходность) | 0.0153 | ARIMA 0.0137 | классика чуть лучше |\n"
        "| Энергопотребление (кВт·ч/день) | **379** | SARIMA 908 | **Neural SDE** |\n"
        "| Скачки глюкозы CGM (mg/dL) | **7.44** | Persistence 7.75 | **Neural SDE** |\n"
    )
    st.success(
        "**Вывод.** Neural SDE особенно хорош там, где есть нелинейная "
        "стохастическая динамика — глюкоза и энергетика. Для финансов "
        "(почти белый шум) классические модели не уступают."
    )

    c1, c2, c3 = st.columns(3)
    with c1:
        show("three_1.png", "Эксперимент 1 — акции Apple")
    with c2:
        show("three_2.png", "Эксперимент 2 — энергопотребление")
    with c3:
        show("three_3.png", "Эксперимент 3 — скачки глюкозы")

# ── Попытка 2: ветрогенерация ─────────────────────────────────────────────
with tab2:
    st.header("Вероятностный прогноз ветрогенерации")
    st.markdown(
        "Данные GEFCom2014-Wind (зона 1). Расширения базовой модели: обусловленный "
        "дрейф (controlled SDE) и **state-dependent диффузия $\\sigma_\\theta$**. "
        "Будущий ветер берём из NWP-прогноза — это легально, не утечка."
    )

    st.subheader("Что получилось")
    st.markdown(
        "- **RMSE-skill vs persistence ≈ +0.29.** По горизонту: $h{=}1$ ≈ −0.10 "
        "(persistence честно сильнее на 1 шаг), $h{=}6$ ≈ +0.28, $h{=}12$ ≈ +0.41.\n"
        "- **Pinball-loss:** NSDE ≈ 0.047 < climatology ≈ 0.074 < persistence ≈ 0.095.\n"
        "- **Coverage80 ≈ 0.86 без пост-обработки** — интервал калибрует сам "
        "$\\sigma_\\theta$.\n"
        "- **$\\sigma_\\theta$ растёт со скоростью ветра** (≈0.12 → 0.19 → 0.38) — "
        "выученная гетероскедастичность с физическим смыслом (кубическая зона "
        "турбины = высокая чувствительность)."
    )

    c1, c2 = st.columns(2)
    with c1:
        show("wind_3.png", "Прогноз и доверительный интервал")
        show("wind_1.png", "σ как функция скорости ветра")
    with c2:
        show("wind_2.png", "Сравнение: NSDE vs persistence vs climatology")
        show("wind_4.png", "Калибровка / skill по горизонту")

    st.success(
        "**Вывод.** На многошаговом горизонте Neural SDE заметно обходит "
        "persistence и климатологию и сам выдаёт калиброванный интервал — то, "
        "ради чего и нужен стохастический подход."
    )

# ── Попытка 3: глюкоза OhioT1DM ───────────────────────────────────────────
with tab3:
    st.header("Глюкоза CGM — augmented-state Neural SDE")
    st.markdown(
        "Данные OhioT1DM (CGM каждые 5 минут). Здесь добавили **state-dependent "
        "диффузию** и **augmented-состояние** (латент с памятью о тренде), чтобы "
        "ловить динамику на длинном горизонте."
    )

    st.subheader("Что получилось")
    st.markdown(
        "- State-dependent диффузия $g_\\theta(X,u)$ оживляет распределенческую "
        "часть: появляются осмысленные **CRPS**, калиброванный интервал "
        "(**coverage80**) и **P(гипо)** — на константной σ их не было.\n"
        "- **Точечный RMSE ≈ persistence** — и это ожидаемо: глюкоза гладкая, "
        "точечная точность упёрта в шум сенсора. Ниша Neural SDE — не точка, а "
        "**распределение**.\n"
        "- **Augmented (память о тренде)** на пациенте 591 обходит и baseline, и "
        "persistence на длинном горизонте: **+60 мин ≈ 34.4 vs 38.1 (baseline) vs "
        "38.9 (persistence)**; на +30 мин разница мала.\n"
        "- Честный потолок: автономный дрейф не предвидит необъявленные события "
        "(еда / болюс инсулина в будущем)."
    )
    st.info(
        "В этом эксперименте графиков из ноутбука нет — ключевые результаты "
        "числовые (CRPS, coverage, RMSE по горизонтам)."
    )
    st.success(
        "**Вывод.** Там, где важно честное предсказательное распределение и риск "
        "(вероятность гипогликемии), Neural SDE даёт то, чего точечные модели не "
        "умеют — даже когда по RMSE они вровень."
    )

# ── Подвал ────────────────────────────────────────────────────────────────
st.markdown("---")
